package qmaks.utils;

public class Point {
    private final int posX;
    private final int posY;

    public Point(int posX, int posY) {
        this.posX = posX;
        this.posY = posY;
    }

    public int getX() {
        return posX;
    }

    public int getY() {
        return posY;
    }
}
